check_difficulty_point:					
	ldrb	r9, [r3, #DIFFICULTY_POINT]		@ 難易度の値を受け取る
	ldrb	r11, [r2, #TORCH_SECONDS]		@ 松明燃焼時間を受け取る
	mov	r10, #3					@ 割り算用に3を取得
