	strb	r11, [r2, #TORCH_SECONDS]		@ 松明燃焼時間を更新する
get_coordinate:
	ldr	r11, =labyrinth_map_coordinate		@ labyrinth_map_coordinateの座標取得
	mov	r10, r12, lsl #2			@ 4倍して座標算出して格納
	add	r11, r11, r10				@ 呼び出す座標算出
	mov	r10, #3					@ loopカウント
	mov	r8, #0					@ 初期化
1:
	ldrb	r9, [r11, r10]				@ 受け取る
	add	r8, r8, r9				@ 足す
	cmp	r10, #0					@ looe_end?
	subne	r10, r10, #1				@ カウントダウン
	movne	r8, r8, lsl #BYTE			@ 受け取ったByteをずらして調整
	bne	1b					@ loop
@ここでr8に呼び出す座標の+α部分が格納される
get_labyrinth:
	ldr	r11, =labyrinth_data_stock		@ ストックポインタ
	add	r11, r11, r8				@ 座標算出
	ldrb	r10, [r2, #LABY_COORDINATE_X]		@ ラビリンスのx方向のサイズ
	ldrb	r9, [r2, #LABY_COORDINATE_Y]		@ ラビリンスのx方向のサイズ
	mul	r10, r9, r10				@ 掛け合わせてラビリンスのサイズを求める
	mov	r8, #0					@ カウント初期化
1:
	ldrb	r9, [r11, r8]				@ データをストックから受け取る
	strb	r9, [r1, r8]				@ ラビリンスのデータを受け取る
	cmp	r8, r10					@ データの呼び出しが終了したか
	addne	r8, r8, #1				@ カウントアップ
	bne	1b					@ loop

@------終了--------------------------------------------
return:
	ldr	r12, [sp], #4		@ pop
	ldr	r11, [sp], #4		@ pop
	ldr	r10, [sp], #4		@ pop
	ldr	r9, [sp], #4		@ pop
	ldr	r8, [sp], #4		@ pop
	ldr	r7, [sp], #4		@ pop
